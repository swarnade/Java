public class Datatypes {
    public static void main(String[] args) {
        byte b=8;
        System.out.println(b);
        char ch='a';
        System.out.println(ch);
        boolean var= true;
        System.out.println(var);
        float price= 10.5f;
        System.out.println(price);
        int i=1;
        System.out.println(i);
        short n=240;
        System.out.println(n);
    }
}
