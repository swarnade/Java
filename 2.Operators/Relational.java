public class Relational {
    public static void main(String[] args) {
        int a=5;
        int b=10;
        int c=5;

        System.out.println(a==c);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=c);
        System.out.println(a!=c);
    }
}
